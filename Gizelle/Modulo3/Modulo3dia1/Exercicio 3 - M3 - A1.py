"""
Faça um programa que leia 5 notas, calcule a média dessas notas e apresente na tela a média
"""

nota1 = int(input("Digite o valor da nota1: "))
nota2 = int(input("Digite o valor da nota2: "))
nota3 = int(input("Digite o valor da nota3: "))
nota4 = int(input("Digite o valor da nota4: "))
nota5 = int(input("Digite o valor da nota5: "))


media = (nota1+nota2+nota3+nota4+nota5)/5

print("A média das 5 notas é: ",media)
