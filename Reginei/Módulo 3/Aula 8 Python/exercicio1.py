'''1. Faça um programa que leia dois números inteiros digitados pelo
teclado e imprima a soma deles.
'''

numero1 = int(input("Digite o Primeiro Número: "))
print(numero1)

numero2 = int(input("Digite o Segundo Número: "))
print(numero2)

soma = numero1 + numero2
print(soma)
print ("A soma dos Números ", numero1, " + ", numero2, " = ", soma)
