'''
2. Faça um programa que leia dois números reais A e B digitados
pelo teclado e imprima a divisão de A por B.
'''

numero1 = int(input("Digite o Número A: "))
print(numero1)

numero2 = int(input("Digite o Número B: "))
print(numero2)

divisao = numero1 / numero2
print(divisao)
print ("A divisao dos Números ", numero1, " / ", numero2, " = ", divisao)


