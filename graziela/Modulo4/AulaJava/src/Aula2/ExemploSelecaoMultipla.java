package Aula2;

public class ExemploSelecaoMultipla {
	public static void main(String args[]) {
		int numero = 2;
		switch (numero) {
			case 0:
				System.out.println("Opção 0");
				break;
			case 1:
				System.out.println("Opção 1");
				break;
			case 2:
				System.out.println("Opção 2");
				break;
			default:
				System.out.println("Opção inválida");
		}
	}
}
